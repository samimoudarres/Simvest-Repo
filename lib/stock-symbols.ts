// Enhanced search functionality
export function searchSymbols(query: string): Array<{ symbol: string; name: string; type: string; sector?: string }> {
  const normalizedQuery = query.toLowerCase().trim()
  const results: Array<{ symbol: string; name: string; type: string; sector?: string }> = []

  // Search by symbol first (exact matches get priority)
  for (const symbol of UNIQUE_SYMBOLS) {
    if (symbol.toLowerCase() === normalizedQuery) {
      results.unshift({
        symbol,
        name: COMPANY_NAMES[symbol] || symbol,
        type: getSymbolType(symbol),
        sector: getSymbolSector(symbol),
      })
    } else if (symbol.toLowerCase().includes(normalizedQuery)) {
      results.push({
        symbol,
        name: COMPANY_NAMES[symbol] || symbol,
        type: getSymbolType(symbol),
        sector: getSymbolSector(symbol),
      })
    }
  }

  // Search by company name
  for (const [symbol, name] of Object.entries(COMPANY_NAMES)) {
    if (name.toLowerCase().includes(normalizedQuery) && !results.find((r) => r.symbol === symbol)) {
      results.push({
        symbol,
        name,
        type: getSymbolType(symbol),
        sector: getSymbolSector(symbol),
      })
    }
  }

  return results.slice(0, 15) // Limit to top 15 results
}

function getSymbolType(symbol: string): string {
  if (CRYPTO_SYMBOLS.includes(symbol)) return "Cryptocurrency"
  if (SECTOR_ETFS.includes(symbol)) return "ETF"
  return "Stock"
}

function getSymbolSector(symbol: string): string {
  if (TECH_STOCKS.includes(symbol)) return "Technology"
  if (HEALTHCARE_STOCKS.includes(symbol)) return "Healthcare"
  if (FINANCE_STOCKS.includes(symbol)) return "Finance"
  if (ENERGY_STOCKS.includes(symbol)) return "Energy"
  if (CRYPTO_SYMBOLS.includes(symbol)) return "Cryptocurrency"
  if (SECTOR_ETFS.includes(symbol)) return "ETF"
  return "Other"
}

// Get random stocks for discovery
export function getRandomStocks(count = 10): string[] {
  const shuffled = [...POPULAR_STOCKS].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}

